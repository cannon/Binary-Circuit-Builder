package com.github.jacksonc.circuitbuilder;

public class DrawInfo {
	public int x;
	public int y;
	public int width;
	public int height;
	public String name;
	
	public DrawInfo(int x, int y, int width, int height, String name) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.name = name;
	}

}
